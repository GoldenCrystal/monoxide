using System;

namespace System.MacOS.AppKit
{
	/// <summary>Indicates the style of an alert.</summary>
	public enum AlertStyle
	{
		/// <summary>An alert show for a warning purpose.</summary>
		Warning = 0,
		/// <summary>An alert shown for an informational purpose.</summary>
		Informational = 1,
		/// <summary>An alert shown for a critical purpose.</summary>
		Critical = 2
	}
}
